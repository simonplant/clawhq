def cmd_draft_reply(args):
    token = _get_token()
    session = _get_session(token)
    body = _read_body()

    # Get original email
    resp = _api_call(session, token, [
        ["Email/get", {
            "accountId": session["accountId"],
            "ids": [args.id],
            "properties": ["id", "from", "replyTo", "subject", "messageId",
                           "references", "textBody", "bodyValues"],
            "fetchTextBodyValues": True,
        }, "eg0"],
    ])

    emails = resp["methodResponses"][0][1].get("list", [])
    if not emails:
        print(f"Error: Email {args.id} not found", file=sys.stderr)
        sys.exit(1)

    orig = emails[0]
    reply_to = orig.get("replyTo") or orig.get("from") or []
    subject = orig.get("subject", "")
    if not subject.lower().startswith("re:"):
        subject = f"Re: {subject}"

    message_id = (orig.get("messageId") or [None])[0]
    references = list(orig.get("references") or [])
    if message_id:
        references.append(message_id)

    orig_body = ""
    for part in orig.get("textBody") or []:
        pid = part.get("partId")
        if pid and pid in orig.get("bodyValues", {}):
            orig_body += orig["bodyValues"][pid].get("value", "")

    quoted = "\n".join(f"> {line}" for line in orig_body.splitlines())
    full_body = f"{body}\n\n{quoted}"

    drafts_id = _get_mailbox_id(session, token, "drafts")

    email_obj = {
        "to": reply_to,
        "subject": subject,
        "inReplyTo": [message_id] if message_id else None,
        "references": references if references else None,
        "mailboxIds": {drafts_id: True},
        "keywords": {"$draft": True},
        "bodyValues": {"body": {"value": full_body, "charset": "utf-8"}},
        "textBody": [{"partId": "body", "type": "text/plain"}],
    }
    email_obj = {k: v for k, v in email_obj.items() if v is not None}

    if args.dry_run:
        print(f"[DRY RUN] Would create draft reply to: {[a.get('email') for a in reply_to]}")
        return

    _audit_log("draft-reply", messageId=args.id)
    resp = _api_call(session, token, [
        ["Email/set", {
            "accountId": session["accountId"],
            "create": {"draft": email_obj},
        }, "es0"],
    ])

    created = resp["methodResponses"][0][1].get("created", {})
    if "draft" in created:
        print(f"Draft reply saved: {created['draft']['id']}")
    else:
        not_created = resp["methodResponses"][0][1].get("notCreated", {})
        print(f"Error: {not_created}", file=sys.stderr)
        sys.exit(1)


def cmd_send_draft(args):
    token = _get_token()
    session = _get_session(token)

    if args.dry_run:
        print(f"[DRY RUN] Would send draft: {args.id}")
        return

    _check_send_rate()

    identity_id = session.get("identityId")
    if not identity_id:
        print("Error: No identity found. Cannot send.", file=sys.stderr)
        sys.exit(1)

    drafts_id = _get_mailbox_id(session, token, "drafts")
    sent_id = _get_mailbox_id(session, token, "sent")

    _audit_log("send-draft", messageId=args.id)
    resp = _api_call(session, token, [
        ["EmailSubmission/set", {
            "accountId": session["accountId"],
            "create": {
                "sub": {
                    "identityId": identity_id,
                    "emailId": args.id,
                },
            },
            "onSuccessUpdateEmail": {
                "#sub": {
                    "mailboxIds/" + drafts_id: None,
                    "mailboxIds/" + sent_id: True,
                    "keywords/$draft": None,
                },
            },
        }, "ss0"],
    ])

    sub_created = resp["methodResponses"][0][1].get("created", {})
    if "sub" in sub_created:
        _record_send()
        print("Draft sent.")
    else:
        not_created = resp["methodResponses"][0][1].get("notCreated", {})
        print(f"Error: {not_created}", file=sys.stderr)
        sys.exit(1)


# ── Follow-ups ─────────────────────────────────────────────────────────────────

def cmd_followup(args):
    state = _load_state()
    followups = state.get("followups", [])

    if args.followup_cmd == "add":
        entry = {
            "messageId": args.id,
            "addedAt": datetime.datetime.now(datetime.timezone.utc).isoformat(),
            "due": args.due,
            "done": False,
        }
        followups.append(entry)
        state["followups"] = followups
        _save_state(state)
        print(f"Follow-up added for {args.id}" + (f" (due: {args.due})" if args.due else ""))

    elif args.followup_cmd == "list":
        pending = [f for f in followups if not f.get("done")]
        if not pending:
            print("No pending follow-ups.")
            return
        today = datetime.date.today().isoformat()
        for f in pending:
            due_date = f.get("due")
            if due_date and due_date <= today:
                status = " ⚠️ OVERDUE"
            elif due_date:
                status = f" [due: {due_date}]"
            else:
                status = ""
            print(f"  {f['messageId']} — added {f['addedAt'][:10]}{status}")

    elif args.followup_cmd == "done":
        found = False
        for f in followups:
            if f["messageId"] == args.id:
                f["done"] = True
                f["doneAt"] = datetime.datetime.now(datetime.timezone.utc).isoformat()
                found = True
                break
        if found:
            state["followups"] = followups
            _save_state(state)
            print(f"Follow-up resolved: {args.id}")
        else:
            print(f"Error: No follow-up found for {args.id}", file=sys.stderr)
            sys.exit(1)


