def _format_addr(addr):
    if not addr:
        return None
    if isinstance(addr, list):
        return [{"name": a.get("name", ""), "email": a.get("email", "")} for a in addr]
    return {"name": addr.get("name", ""), "email": addr.get("email", "")}


def _format_triage(emails, contacts, mailbox_map=None, thread_counts=None):
    result = []
    for e in emails:
        from_addr = (e.get("from") or [{}])[0]
        from_email = from_addr.get("email", "")
        known, ctx = _is_known_contact(from_email, contacts)

        # Determine folder name from mailboxIds
        folder = "INBOX"
        if mailbox_map and e.get("mailboxIds"):
            for mid in e["mailboxIds"]:
                if mid in mailbox_map:
                    folder = mailbox_map[mid]
                    break

        # isReply: check inReplyTo header, not $answered keyword
        in_reply_to = e.get("inReplyTo")
        is_reply = bool(in_reply_to and len(in_reply_to) > 0)

        thread_id = e.get("threadId")
        thread_size = (thread_counts or {}).get(thread_id)

        item = {
            "id": e.get("id"),
            "threadId": thread_id,
            "from": _format_addr(from_addr),
            "replyTo": _format_addr((e.get("replyTo") or [None])[0]),
            "to": _format_addr(e.get("to") or []),
            "cc": _format_addr(e.get("cc") or []),
            "subject": _cw(e.get("subject", ""), sender=from_email),
            "date": e.get("receivedAt", ""),
            "preview": _cw(e.get("preview", ""), sender=from_email),
            "size": e.get("size", 0),
            "hasAttachments": e.get("hasAttachment", False),
            "threadSize": thread_size,
            "isReply": is_reply,
            "folder": folder,
            "listId": e.get("header:list-id:asText"),
            "unsubscribeUrl": e.get("header:List-Unsubscribe"),
            "knownContact": known,
            "contactContext": ctx if known else None,
        }
        result.append(item)
    return result

