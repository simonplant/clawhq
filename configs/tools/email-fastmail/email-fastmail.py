    if not emails:
        print(f"Error: Email {args.id} not found", file=sys.stderr)
        sys.exit(1)

    attachment = None
    for att in emails[0].get("attachments") or []:
        if att.get("partId") == args.part_id:
            attachment = att
            break

    if not attachment:
        print(f"Error: Attachment {args.part_id} not found", file=sys.stderr)
        sys.exit(1)

    blob_id = attachment["blobId"]
    filename = attachment.get("name", f"attachment-{args.part_id}")

    # Build download URL
    download_url = session["downloadUrl"]
    download_url = download_url.replace("{accountId}", session["accountId"])
    download_url = download_url.replace("{blobId}", blob_id)
    download_url = download_url.replace("{name}", urllib.request.quote(filename))
    download_url = download_url.replace("{type}", attachment.get("type", "application/octet-stream"))

    os.makedirs(MEDIA_DIR, exist_ok=True)
    filepath = os.path.join(MEDIA_DIR, filename)

    headers = {"Authorization": f"Bearer {token}"}
    req = urllib.request.Request(download_url, headers=headers)
    with urllib.request.urlopen(req, timeout=TIMEOUT) as resp:
        with open(filepath, "wb") as f:
            f.write(resp.read())

    print(f"Downloaded: {filepath}")


