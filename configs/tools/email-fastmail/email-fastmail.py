def cmd_triage(args):
    token = _get_token()
    session = _get_session(token)
    inbox_id = _get_mailbox_id(session, token, "inbox")
    contacts = _load_contacts()

    limit = args.limit or 50

    method_calls = [
        ["Email/query", {
            "accountId": session["accountId"],
            "filter": {
                "inMailbox": inbox_id,
                "notKeyword": "$seen",
            },
            "sort": [{"property": "receivedAt", "isAscending": False}],
            "limit": limit,
        }, "eq0"],
        ["Email/get", {
            "accountId": session["accountId"],
            "#ids": {"resultOf": "eq0", "name": "Email/query", "path": "/ids"},
            "properties": TRIAGE_PROPERTIES,
            "fetchTextBodyValues": True,
        }, "eg0"],
    ]

    resp = _api_call(session, token, method_calls)

    if args.count:
        total = resp["methodResponses"][0][1].get("total", 0)
        print(total)
        return

    emails = resp["methodResponses"][1][1].get("list", [])

    # Fetch thread sizes for context
    thread_counts = {}
    thread_ids = list({e["threadId"] for e in emails if e.get("threadId")})
    if thread_ids:
        t_resp = _api_call(session, token, [
            ["Thread/get", {
                "accountId": session["accountId"],
                "ids": thread_ids,
            }, "tg0"],
        ])
        for t in t_resp["methodResponses"][0][1].get("list", []):
            thread_counts[t["id"]] = len(t.get("emailIds", []))

    result = _format_triage(emails, contacts, thread_counts=thread_counts)
    print(json.dumps(result, indent=2, ensure_ascii=False))


