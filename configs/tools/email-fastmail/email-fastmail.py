def cmd_batch(args):
    """Process batch operations from stdin JSON.

    Merges multiple actions for the same email into one JMAP update.
    Archive and delete implicitly mark as read.
    """
    data = json.loads(sys.stdin.read())
    if not isinstance(data, list):
        print("Error: Expected JSON array on stdin", file=sys.stderr)
        sys.exit(1)

    if args.dry_run:
        for op in data:
            print(f"[DRY RUN] {op.get('action', '?')}: {op.get('id', '?')}")
        return

    token = _get_token()
    session = _get_session(token)

    # Collect all email IDs to fetch current mailbox state
    all_ids = list({op["id"] for op in data if op.get("id")})
    if not all_ids:
        return

    # Fetch current mailboxes for all emails in one call
    resp = _api_call(session, token, [
        ["Email/get", {
            "accountId": session["accountId"],
            "ids": all_ids,
            "properties": ["mailboxIds"],
        }, "eg0"],
    ])
    email_mailboxes = {}
    for e in resp["methodResponses"][0][1].get("list", []):
        email_mailboxes[e["id"]] = set(e.get("mailboxIds", {}).keys())

    # Preload mailbox IDs lazily
    archive_id = None
    spam_id = None

    # Merge actions per email ID (multiple actions on same email → single update)
    updates = {}
    for op in data:
        eid = op.get("id")
        action = op.get("action")
        if not eid or not action:
            print(f"Warning: Skipping invalid batch entry: {op}", file=sys.stderr)
            continue

        _audit_log(action, messageId=eid, reason=op.get("reason", "batch"))

        if eid not in updates:
            updates[eid] = {}

        if action == "archive":
            if not archive_id:
                archive_id = _get_mailbox_id(session, token, "archive")
                if not archive_id:
                    archive_id = _ensure_mailbox(session, token, "Archive")
            updates[eid]["keywords/$seen"] = True
            updates[eid][f"mailboxIds/{archive_id}"] = True
            for mid in email_mailboxes.get(eid, set()):
                if mid != archive_id:
                    updates[eid][f"mailboxIds/{mid}"] = None
        elif action == "delete":
            if not spam_id:
                spam_id = _ensure_mailbox(session, token, "ClawHQ-Spam")
            updates[eid]["keywords/$seen"] = True
            updates[eid][f"mailboxIds/{spam_id}"] = True
            for mid in email_mailboxes.get(eid, set()):
                if mid != spam_id:
                    updates[eid][f"mailboxIds/{mid}"] = None
        elif action == "mark-read":
            updates[eid]["keywords/$seen"] = True
        elif action == "label":
            kw = op.get("keyword")
            if kw:
                updates[eid][f"keywords/{kw}"] = True
        elif action == "unlabel":
            kw = op.get("keyword")
            if kw:
                updates[eid][f"keywords/{kw}"] = None
        else:
            print(f"Warning: Unknown action '{action}' for {eid}", file=sys.stderr)

    if not updates:
        return

    _api_call(session, token, [
        ["Email/set", {
            "accountId": session["accountId"],
            "update": updates,
        }, "es0"],
    ])
    print(f"Batch: processed {len(updates)} email(s).")


