# ── JMAP Session ───────────────────────────────────────────────────────────────

def _get_token():
    # Auth injected by credential proxy (SEC-022) — return dummy for API compat
    if not _CRED_PROXY_URL:
        print("Error: CRED_PROXY_URL not set", file=sys.stderr)
        sys.exit(1)
    return "proxy"


def _rewrite_url(url):
    """Rewrite FastMail absolute URLs to route through credential proxy."""
    if not _CRED_PROXY_URL:
        return url
    url = url.replace("https://api.fastmail.com", f"{_CRED_PROXY_URL}/fastmail")
    url = url.replace("https://www.fastmailusercontent.com", f"{_CRED_PROXY_URL}/fastmail-dl")
    return url


def _jmap_request(url, token, data=None, method="GET"):
    url = _rewrite_url(url)
    headers = {
        "Content-Type": "application/json",
    }
    body = json.dumps(data).encode() if data else None
    req = urllib.request.Request(url, data=body, headers=headers, method=method)
    resp = urllib.request.urlopen(req, timeout=TIMEOUT)
    return json.loads(resp.read().decode())


def _get_session(token, force=False):
    state = _load_state()
    cached = state.get("session")
    if not force and cached:
        if time.time() - cached.get("fetched_at", 0) < SESSION_TTL:
            return cached

    try:
        sess = _jmap_request(JMAP_WELL_KNOWN, token)
    except urllib.error.HTTPError as e:
        if e.code in (401, 403):
            print("Error: FastMail API token invalid or expired", file=sys.stderr)
            sys.exit(1)
        raise

    account_id = sess.get("primaryAccounts", {}).get("urn:ietf:params:jmap:mail")
    if not account_id:
        print("Error: No mail account found in JMAP session", file=sys.stderr)
        sys.exit(1)

    api_url = sess.get("apiUrl")
    download_url = sess.get("downloadUrl")

    # Fetch identity
    identity_id = None
    try:
        id_resp = _jmap_request(api_url, token, {
            "using": ["urn:ietf:params:jmap:core", "urn:ietf:params:jmap:submission"],
            "methodCalls": [["Identity/get", {"accountId": account_id}, "id0"]],
        }, method="POST")
        identities = id_resp["methodResponses"][0][1].get("list", [])
        if identities:
            identity_id = identities[0]["id"]
    except Exception as e:
        print(f"Warning: Could not fetch identity: {e}", file=sys.stderr)

    cached = {
        "accountId": account_id,
        "apiUrl": api_url,
        "downloadUrl": download_url,
        "identityId": identity_id,
        "fetched_at": time.time(),
    }
    state["session"] = cached
    _save_state(state)
    return cached


def _api_call(session, token, method_calls, retries=1):
    data = {
        "using": [
            "urn:ietf:params:jmap:core",
            "urn:ietf:params:jmap:mail",
            "urn:ietf:params:jmap:submission",
        ],
        "methodCalls": method_calls,
    }
    try:
        resp = _jmap_request(session["apiUrl"], token, data, method="POST")
    except urllib.error.HTTPError as e:
        if e.code in (401, 404) and retries > 0:
            session = _get_session(token, force=True)
            return _api_call(session, token, method_calls, retries=retries - 1)
        raise

    # Check for errors in method responses
    for mr in resp.get("methodResponses", []):
        if mr[0] == "error":
            print(f"JMAP error: {mr[1]}", file=sys.stderr)
    return resp


def _get_mailbox_id(session, token, role):
    """Get mailbox ID by role (inbox, archive, drafts, sent, trash, junk)."""
    state = _load_state()
    cache_key = f"mailbox_{role}"
    cached = state.get(cache_key)
    if cached:
        return cached

    resp = _api_call(session, token, [
        ["Mailbox/query", {
            "accountId": session["accountId"],
            "filter": {"role": role},
        }, "mq0"],
        ["Mailbox/get", {
            "accountId": session["accountId"],
            "#ids": {"resultOf": "mq0", "name": "Mailbox/query", "path": "/ids"},
        }, "mg0"],
    ])

    mailboxes = resp["methodResponses"][1][1].get("list", [])
    if mailboxes:
        mid = mailboxes[0]["id"]
        state[cache_key] = mid
        _save_state(state)
        return mid
    return None


def _get_mailbox_id_by_name(session, token, name):
    """Get mailbox ID by name."""
    resp = _api_call(session, token, [
        ["Mailbox/get", {
            "accountId": session["accountId"],
        }, "mg0"],
    ])
    for mb in resp["methodResponses"][0][1].get("list", []):
        if mb.get("name") == name:
            return mb["id"]
    return None


def _ensure_mailbox(session, token, name):
    """Ensure a mailbox exists, create if missing. Returns mailbox ID."""
    mid = _get_mailbox_id_by_name(session, token, name)
    if mid:
        return mid

    resp = _api_call(session, token, [
        ["Mailbox/set", {
            "accountId": session["accountId"],
            "create": {
                "new": {"name": name},
            },
        }, "ms0"],
    ])
    created = resp["methodResponses"][0][1].get("created", {})
    if "new" in created:
        return created["new"]["id"]
    print(f"Error: Could not create mailbox '{name}'", file=sys.stderr)
    sys.exit(1)

# ── Rate Limiting ──────────────────────────────────────────────────────────────
# Burst rate limiter: blocks if >BURST_LIMIT sends in BURST_WINDOW seconds.
# Catches runaway loops without capping legitimate daily volume.

def _check_send_rate():
    state = _load_state()
    now = time.time()
    timestamps = state.get("send_timestamps", [])
    # Prune old entries outside the window
    timestamps = [t for t in timestamps if now - t < BURST_WINDOW]
    if len(timestamps) >= BURST_LIMIT:
        print(f"Error: Send rate limit hit ({BURST_LIMIT} sends in {BURST_WINDOW // 60} min). "
              f"Wait a few minutes — this prevents runaway loops.", file=sys.stderr)
        sys.exit(1)


def _record_send():
    state = _load_state()
    now = time.time()
    timestamps = state.get("send_timestamps", [])
    timestamps = [t for t in timestamps if now - t < BURST_WINDOW]
    timestamps.append(now)
    state["send_timestamps"] = timestamps
    _save_state(state)

