def cmd_reply(args):
    # Delegated reply validation
    if args.delegated:
        ok, reason = _validate_delegated(args.delegated, "reply")
        if not ok:
            print(f"Error: Delegated reply rejected — {reason}", file=sys.stderr)
            sys.exit(1)

    token = _get_token()
    session = _get_session(token)
    body = _read_body()

    # Get original email
    resp = _api_call(session, token, [
        ["Email/get", {
            "accountId": session["accountId"],
            "ids": [args.id],
            "properties": ["id", "from", "to", "cc", "replyTo", "subject", "messageId",
                           "references", "textBody", "bodyValues"],
            "fetchTextBodyValues": True,
        }, "eg0"],
    ])

    emails = resp["methodResponses"][0][1].get("list", [])
    if not emails:
        print(f"Error: Email {args.id} not found", file=sys.stderr)
        sys.exit(1)

    orig = emails[0]
    reply_to = orig.get("replyTo") or orig.get("from") or []
    subject = orig.get("subject", "")
    if not subject.lower().startswith("re:"):
        subject = f"Re: {subject}"

    message_id = (orig.get("messageId") or [None])[0]
    references = list(orig.get("references") or [])
    if message_id:
        references.append(message_id)

    # Quote original
    orig_body = ""
    for part in orig.get("textBody") or []:
        pid = part.get("partId")
        if pid and pid in orig.get("bodyValues", {}):
            orig_body += orig["bodyValues"][pid].get("value", "")

    quoted = "\n".join(f"> {line}" for line in orig_body.splitlines())
    full_body = f"{body}\n\n{quoted}"

    to = reply_to
    if args.reply_all:
        # Add original To and CC, excluding our own address
        # Fetch our email to filter it out
        own_email = None
        identity_id = session.get("identityId")
        if identity_id:
            try:
                id_resp = _api_call(session, token, [
                    ["Identity/get", {
                        "accountId": session["accountId"],
                        "ids": [identity_id],
                    }, "ig0"],
                ])
                identities = id_resp["methodResponses"][0][1].get("list", [])
                if identities:
                    own_email = identities[0].get("email", "").lower()
            except Exception:
                pass

        to = list(reply_to)
        for addr in (orig.get("to") or []) + (orig.get("cc") or []):
            addr_email = addr.get("email", "").lower()
            if own_email and addr_email == own_email:
                continue
            # Avoid duplicates with reply_to
            if not any(a.get("email", "").lower() == addr_email for a in to):
                to.append(addr)

    email_obj = {
        "to": to,
        "subject": subject,
        "inReplyTo": [message_id] if message_id else None,
        "references": references if references else None,
        "bodyValues": {"body": {"value": full_body, "charset": "utf-8"}},
        "textBody": [{"partId": "body", "type": "text/plain"}],
    }
    # Remove None values
    email_obj = {k: v for k, v in email_obj.items() if v is not None}

    _audit_log("reply", messageId=args.id, to=[a.get("email") for a in to],
               delegated=args.delegated or False)
    _create_email_and_submit(session, token, email_obj, dry_run=args.dry_run)


