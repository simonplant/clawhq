def cmd_audit(args):
    last = args.last or 20
    try:
        with open(AUDIT_FILE, "r") as f:
            lines = f.readlines()
    except FileNotFoundError:
        print("No audit log yet.")
        return

    for line in lines[-last:]:
        try:
            entry = json.loads(line)
            ts = entry.get("timestamp", "")[:19]
            action = entry.get("action", "?")
            mid = entry.get("messageId", "")
            subj = entry.get("subject", "")
            extra = f" — {subj}" if subj else ""
            if entry.get("to"):
                extra = f" → {entry['to']}"
            print(f"  {ts}  {action:12s}  {mid}{extra}")
        except json.JSONDecodeError:
            continue


def cmd_audit_summary(args):
    try:
        with open(AUDIT_FILE, "r") as f:
            lines = f.readlines()
    except FileNotFoundError:
        print("No audit log yet.")
        return

    cutoff = (datetime.datetime.now(datetime.timezone.utc) - datetime.timedelta(hours=24)).isoformat()
    counts = {}
    for line in lines:
        try:
            entry = json.loads(line)
            if entry.get("timestamp", "") >= cutoff:
                action = entry.get("action", "other")
                counts[action] = counts.get(action, 0) + 1
        except json.JSONDecodeError:
            continue

    total = sum(counts.values())
    print(f"Last 24 hours: {total} actions")
    for action, count in sorted(counts.items(), key=lambda x: -x[1]):
        print(f"  {action}: {count}")


