def _read_body():
    if sys.stdin.isatty():
        print("Error: Body must be piped via stdin", file=sys.stderr)
        sys.exit(1)
    return sys.stdin.read()


def _create_email_and_submit(session, token, email_obj, dry_run=False):
    """Create an email in Drafts and submit it."""
    # ClawWall egress: scan subject + body for leaked secrets before sending
    egress_text = email_obj.get("subject", "")
    body_vals = email_obj.get("bodyValues", {})
    for bv in body_vals.values():
        if isinstance(bv, dict):
            egress_text += " " + bv.get("value", "")
    ok, leaks = _clawwall_egress(egress_text, source="email-fastmail")
    if not ok:
        labels = ", ".join(lk.label() for lk in leaks)
        print(f"EGRESS BLOCKED: secrets detected in outbound email ({labels}). Send aborted.", file=sys.stderr)
        sys.exit(3)

    if dry_run:
        print(f"[DRY RUN] Would send to: {email_obj.get('to', [])}")
        print(f"  Subject: {email_obj.get('subject', '')}")
        return

    _check_send_rate()

    identity_id = session.get("identityId")
    if not identity_id:
        print("Error: No identity found. Cannot send.", file=sys.stderr)
        sys.exit(1)

    drafts_id = _get_mailbox_id(session, token, "drafts")
    sent_id = _get_mailbox_id(session, token, "sent")

    email_obj["mailboxIds"] = {drafts_id: True}
    email_obj["keywords"] = {"$draft": True}

    resp = _api_call(session, token, [
        ["Email/set", {
            "accountId": session["accountId"],
            "create": {"draft": email_obj},
        }, "es0"],
        ["EmailSubmission/set", {
            "accountId": session["accountId"],
            "create": {
                "sub": {
                    "identityId": identity_id,
                    "emailId": "#draft",
                },
            },
            "onSuccessUpdateEmail": {
                "#sub": {
                    "mailboxIds/" + drafts_id: None,
                    "mailboxIds/" + sent_id: True,
                    "keywords/$draft": None,
                },
            },
        }, "ss0"],
    ])

    # Check for errors
    created = resp["methodResponses"][0][1].get("created", {})
    if "draft" not in created:
        not_created = resp["methodResponses"][0][1].get("notCreated", {})
        print(f"Error creating email: {not_created}", file=sys.stderr)
        sys.exit(1)

    sub_created = resp["methodResponses"][1][1].get("created", {})
    if "sub" not in sub_created:
        not_created = resp["methodResponses"][1][1].get("notCreated", {})
        print(f"Error submitting email: {not_created}", file=sys.stderr)
        sys.exit(1)

    _record_send()
    print("Sent.")


