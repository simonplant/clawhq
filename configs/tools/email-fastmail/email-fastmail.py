def cmd_contacts(args):
    contacts = _load_contacts()

    if args.contacts_cmd == "list":
        for c in contacts.get("contacts", []):
            ctx = f" — {c['context']}" if c.get("context") else ""
            trust = f" [{c['trust']}]" if c.get("trust") else ""
            print(f"  {c['name']} <{c['email']}>{trust}{ctx}")

    elif args.contacts_cmd == "add":
        # Check for duplicates
        existing = {c["email"].lower() for c in contacts.get("contacts", [])}
        if args.email.lower() in existing:
            print(f"Error: {args.email} already in contacts. Use 'fm contacts find' to see it.", file=sys.stderr)
            sys.exit(1)

        entry = {
            "email": args.email,
            "name": args.name,
            "context": args.context or "",
            "trust": "known",
            "addedAt": datetime.datetime.now(datetime.timezone.utc).isoformat(),
        }
        contacts.setdefault("contacts", []).append(entry)
        d = os.path.dirname(CONTACTS_FILE)
        fd, tmp = tempfile.mkstemp(dir=d, suffix=".tmp")
        with os.fdopen(fd, "w") as f:
            json.dump(contacts, f, indent=2, ensure_ascii=False)
        os.replace(tmp, CONTACTS_FILE)
        print(f"Added: {args.name} <{args.email}>")

    elif args.contacts_cmd == "find":
        query = args.query.lower()
        matches = [c for c in contacts.get("contacts", [])
                    if query in c.get("name", "").lower() or query in c.get("email", "").lower()]
        if not matches:
            print("No matches.")
            return
        for c in matches:
            ctx = f" — {c['context']}" if c.get("context") else ""
            print(f"  {c['name']} <{c['email']}>{ctx}")

    elif args.contacts_cmd == "import":
        print("Contact import: scanning sent mail for frequent recipients...")
        token = _get_token()
        session = _get_session(token)
        sent_id = _get_mailbox_id(session, token, "sent")
        if not sent_id:
            print("Error: Sent folder not found", file=sys.stderr)
            sys.exit(1)

        resp = _api_call(session, token, [
            ["Email/query", {
                "accountId": session["accountId"],
                "filter": {"inMailbox": sent_id},
                "sort": [{"property": "receivedAt", "isAscending": False}],
                "limit": 100,
            }, "eq0"],
            ["Email/get", {
                "accountId": session["accountId"],
                "#ids": {"resultOf": "eq0", "name": "Email/query", "path": "/ids"},
                "properties": ["to", "cc"],
            }, "eg0"],
        ])

        emails = resp["methodResponses"][1][1].get("list", [])
        addr_count = {}
        for e in emails:
            for addr in (e.get("to") or []) + (e.get("cc") or []):
                email_addr = addr.get("email", "")
                if email_addr:
                    key = email_addr.lower()
                    if key not in addr_count:
                        addr_count[key] = {"email": email_addr, "name": addr.get("name", ""), "count": 0}
                    addr_count[key]["count"] += 1

        existing = {c["email"].lower() for c in contacts.get("contacts", [])}
        new_contacts = []
        for key, info in sorted(addr_count.items(), key=lambda x: -x[1]["count"]):
            if key not in existing and info["count"] >= 2:
                new_contacts.append(info)

        if not new_contacts:
            print("No new frequent contacts found.")
            return

        print(f"Found {len(new_contacts)} frequent recipients not in contacts:")
        for c in new_contacts[:20]:
            print(f"  {c['name']} <{c['email']}> ({c['count']} emails)")
        print("\nAdd them with: fm contacts add <email> <name>")


