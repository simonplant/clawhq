def _load_state():
    try:
        with open(STATE_FILE, "r") as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def _save_state(state):
    d = os.path.dirname(STATE_FILE)
    fd, tmp = tempfile.mkstemp(dir=d, suffix=".tmp")
    try:
        with os.fdopen(fd, "w") as f:
            json.dump(state, f, indent=2, ensure_ascii=False)
        os.replace(tmp, STATE_FILE)
    except Exception:
        try:
            os.unlink(tmp)
        except OSError:
            pass
        raise


def _load_contacts():
    try:
        with open(CONTACTS_FILE, "r") as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return {"contacts": []}


def _is_known_contact(email, contacts):
    email_lower = email.lower()
    for c in contacts.get("contacts", []):
        if c.get("email", "").lower() == email_lower:
            return True, c.get("context", "")
    return False, ""


def _load_delegated_rules():
    try:
        with open(DELEGATED_RULES_FILE, "r") as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return {"categories": {}}


def _validate_delegated(category, action, subject=""):
    """Validate a delegated send against approved categories.

    Returns (ok: bool, reason: str).
    """
    rules = _load_delegated_rules()
    cats = rules.get("categories", {})
    if category not in cats:
        return False, f"Unknown delegated category: {category}"
    cat = cats[category]
    allowed = cat.get("allowed_actions", [])
    if not allowed:
        return False, f"Category '{category}' is disabled"
    if action not in allowed:
        return False, f"Action '{action}' not allowed for category '{category}' (allowed: {allowed})"
    return True, "ok"


