/**
 * dnsmasq config generation for the dns-resolver sidecar.
 *
 * The egress firewall is ipset-based: only IPs in `clawhq_egress` are
 * allowed out. CDN-fronted APIs (Apigee, CloudFront, Cloudflare) return
 * different A records per query and may rotate the resolved IP between
 * the time the firewall snapshots the allowlist and the time the
 * container actually connects. The result is silent egress drops.
 *
 * The fix: a dnsmasq sidecar that intercepts every container DNS query.
 * For each allowlisted FQDN, dnsmasq emits an `ipset=` directive that
 * automatically adds the resolved IP to `clawhq_egress` on every reply.
 * The next container connect therefore always finds its IP in the set.
 *
 * Containers point their `dns:` at the bridge gateway IP where dnsmasq
 * listens; dnsmasq runs in the host network namespace (the only place
 * the host's ipset is visible) with CAP_NET_ADMIN so it can write to
 * the set via netlink.
 *
 * See knowledge/wiki/decisions/cdn-dns-aware-egress.md for context.
 */

import type { FirewallAllowEntry } from "./types.js";

/** Upstream DNS servers used by the resolver — IP literals so there is no
 * chicken-and-egg with our own DNS resolution at boot. */
export const DNS_RESOLVER_UPSTREAMS: readonly string[] = [
  "1.1.1.1",
  "1.0.0.1",
  "8.8.8.8",
];

/**
 * Render a dnsmasq.conf that:
 *   - Listens only on `listenAddress` (bridge gateway).
 *   - Forwards all queries to IP-literal upstreams.
 *   - For every allowlisted FQDN, auto-populates `clawhq_egress` (and
 *     `clawhq_egress_v6`) on every successful resolve.
 *
 * Domains are deduplicated and sorted for stable output (apply diffs are
 * noiseless). Wildcard entries are not generated — dnsmasq's `ipset=` is
 * suffix-matched, so `ipset=/api.tradier.com/clawhq_egress` already
 * captures `quotes.api.tradier.com` and similar subdomains.
 */
export function renderDnsmasqConf(
  allowlist: readonly FirewallAllowEntry[],
  listenAddress: string,
  ipsetName = "clawhq_egress",
  ipsetNameV6 = "clawhq_egress_v6",
): string {
  const domains = [...new Set(allowlist.map((e) => e.domain))]
    .filter((d) => isResolvableHostname(d))
    .sort();

  const lines: string[] = [
    "# Auto-generated by ClawHQ. Do not edit.",
    "# Source: ops/firewall/allowlist.yaml",
    "#",
    "# This config drives the dns-resolver sidecar. Every container DNS",
    "# query for an allowlisted FQDN is resolved by dnsmasq, and the",
    "# resolved IPs are written to the host's clawhq_egress ipset before",
    "# the reply is returned. This makes the IP allowlist self-healing",
    "# against CDN A-record rotation.",
    "",
    "# ── Listener ────────────────────────────────────────────────────",
    `listen-address=${listenAddress}`,
    "bind-interfaces",
    "port=53",
    "",
    "# ── Upstream resolvers (IP literals — no bootstrap dependency) ──",
    "no-resolv",
    "no-hosts",
    ...DNS_RESOLVER_UPSTREAMS.map((ip) => `server=${ip}`),
    "",
    "# ── Cache ───────────────────────────────────────────────────────",
    "cache-size=1000",
    "neg-ttl=30",
    "",
    "# ── Hardening ───────────────────────────────────────────────────",
    "domain-needed",
    "bogus-priv",
    "stop-dns-rebind",
    "rebind-localhost-ok",
    "",
    "# ── ipset population for egress allowlist ───────────────────────",
  ];

  if (domains.length === 0) {
    lines.push("# (no allowlisted domains — resolver still forwards but populates nothing)");
  } else {
    for (const domain of domains) {
      lines.push(`ipset=/${domain}/${ipsetName},${ipsetNameV6}`);
    }
  }
  lines.push("");
  return lines.join("\n");
}

/**
 * Reject obviously-invalid hostnames before they reach dnsmasq's parser.
 *
 * A bad line in dnsmasq.conf takes the whole resolver down. Allowlist
 * entries with empty strings, leading dots, IP literals, or path-like
 * shapes have all been observed in real-world clawhq.yaml files; treat
 * them as configuration errors and silently skip rather than poisoning
 * the resolver.
 */
function isResolvableHostname(value: string): boolean {
  if (!value || value.length === 0) return false;
  // No leading/trailing dots, no whitespace, no slashes.
  if (/^[.\s]/.test(value)) return false;
  if (/[\s/]/.test(value)) return false;
  // IP literal — useless to add an ipset rule for.
  if (/^\d+\.\d+\.\d+\.\d+$/.test(value)) return false;
  if (/^\[?[0-9a-fA-F:]+\]?$/.test(value) && value.includes(":")) return false;
  // Must contain at least one dot (no bare hostnames).
  if (!value.includes(".")) return false;
  // RFC 1035-ish character class.
  return /^[a-zA-Z0-9._-]+$/.test(value);
}
