// ── Classification API ──────────────────────────────────────────────────────

/**
 * Return the owner of a path, or `null` if no rule matches.
 *
 * @param relativePath — path relative to the deployment directory (no
 *   leading slash, forward-slash separated on all platforms).
 */
export function classify(relativePath: string): Owner | null {
  const rule = classifyRule(relativePath);
  return rule ? rule.owner : null;
}

/**
 * Return the full matching rule (or `null`), so callers can display the
 * `reason` alongside drift reports.
 */
export function classifyRule(relativePath: string): OwnershipRule | null {
  for (const rule of OWNERSHIP_RULES) {
    if (matchesPattern(relativePath, rule.pattern)) return rule;
  }
  return null;
}

function matchesPattern(path: string, pattern: string): boolean {
  // Directory prefix: "ops/audit/**" matches "ops/audit" and anything under
  if (pattern.endsWith("/**")) {
    const prefix = pattern.slice(0, -3);
    return path === prefix || path.startsWith(prefix + "/");
  }
  // Tail suffix: "**/*.bak" matches any path ending in ".bak"
  if (pattern.startsWith("**/*")) {
    const suffix = pattern.slice(3);
    if (suffix.startsWith("*")) {
      return path.endsWith(suffix.slice(1));
    }
  }
  // Exact path match
  return path === pattern;
}
