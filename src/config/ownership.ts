/**
 * Ownership table for the deployment directory (~/.clawhq/).
 *
 * Every file under a deployment has exactly one owner. The owner determines
 * who may write to the file and what happens when its content drifts from
 * the expected state. This is the single source of truth — all future
 * reconciler work (atomic apply, drift detection, unclassified-file audit)
 * reads from this table.
 *
 * This module is correctness scaffolding. It does NOT yet enforce ownership
 * on writes — that's Phase 2. For now it backs two observability tools:
 *   - `deploy-unclassified` doctor check (surfaces files not covered here)
 *   - snapshot-diff tool (groups drift by owner)
 *
 * References:
 *   - docs/ARCHITECTURE.md §703 "The Deployment Directory"
 *   - docs/BLUEPRINT-SPEC.md §143, §531 (identity_mount: read-only is the
 *     security baseline — not a stylistic choice)
 *   - docs/security/INCIDENTS.md §321 (supply-chain skill attack rationale)
 */

// ── Types ───────────────────────────────────────────────────────────────────

export type Owner =
  /** Regenerated by clawhq on every `apply`. Any edit will be reverted.
   *  Identity files fall here per the read-only security baseline. */
  | "clawhq"
  /** Clawhq never touches. User/agent state that must survive everything. */
  | "user"
  /** OpenClaw runtime writes, clawhq only reads. Sessions, cron runtime
   *  state, paired devices, delivery queue, channel pairings. */
  | "openclaw"
  /** Compiled definitions merged with preserved runtime state. Today only
   *  cron/jobs.json and engine/.env (credential protection). */
  | "merged"
  /** Template seeded on first apply, then owned by user or the
   *  `integrate add` flow. Never overwritten after creation. */
  | "seeded-once"
  /** Owned by `clawhq build`, not `clawhq apply`. Compose, Dockerfile,
   *  build manifests, OpenClaw source tree. */
  | "build";

export interface OwnershipRule {
  /** Path relative to the deployment directory. Supports two pattern forms:
   *    - exact match: "workspace/SOUL.md"
   *    - directory prefix: "workspace/memory/**" matches the dir and
   *      everything recursively under it. */
  readonly pattern: string;
  readonly owner: Owner;
  /** Short rationale. Surfaces in `clawhq inspect <path>` and drift reports. */
  readonly reason: string;
}

